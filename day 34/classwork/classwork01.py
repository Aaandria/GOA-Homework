# სბოლოოდ გამოპრინტეთ ახალი tuple ელემენტ შეცვლილი. ახსენით კომენტარებით რომელ ხაზზე რა ხდება.

t_1 = (1,2,3,4,5)  #შეიქმნა tuple
t_1 = list(t_1)   #touple გადაიქცა list-ად
t_1[2] = "changed"  #შეიცვალა list-ის მეორე ინდექსი
t_1 = tuple(t_1)    #list-ი გადაიქცა tuple-ად
print(t_1)  # გამოიპრინტა საბოლოო შედეგი