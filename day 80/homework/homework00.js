// ამოცანა შეასრულეთ reduce მეთოდის გამოყენებით.

const ages = [18, 21, 18, 30, 21, 18, 25]

const ageCount = ages.reduce((acc, age) => {
  acc[age] = (acc[age] || 0) + 1
  return acc
}, {})

